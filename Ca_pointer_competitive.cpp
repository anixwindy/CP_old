#include <iostream>
#include <vector>
#include <cstring>   // memset / memcpy
using namespace std;

// ════════════════════════════════════════════════════════════════
//  指標概念快速複習
//
//  int x = 42;
//  int* p = &x;    p 存著 x 的「記憶體地址」
//  *p              解參考：走到那個地址，讀/寫值
//  *p = 99;        等同於 x = 99
//
//  類比：
//    x   = 一棟房子的內容（住的人）
//    &x  = 那棟房子的地址（門牌號碼）
//    p   = 一張紙，上面寫著門牌號碼
//    *p  = 拿著那張紙，走到那棟房子，開門進去
// ════════════════════════════════════════════════════════════════

// ──────────────────────────────────────
//  競程用法 1：用指標走訪陣列
//  （老式風格，了解概念就好；現在多用 for range）
// ──────────────────────────────────────
void demo_array_traverse() {
    int a[]{3, 1, 4, 1, 5};
    int n = 5;

    // a 本身就是陣列第 0 格的地址
    // a + n 是最後一格的「下一格」地址（哨兵）
    int sum{};
    for (int* p = a; p != a + n; ++p) {
        sum += *p;   // *p 依序是 3,1,4,1,5
    }
    cout << "陣列總和(指標走訪) = " << sum << '\n';

    // 現代競程更常這樣寫，效果完全一樣：
    int sum2{};
    for (int i = 0; i < n; ++i) sum2 += a[i];
    cout << "陣列總和(索引走訪) = " << sum2 << '\n';
}

// ──────────────────────────────────────
//  競程用法 2：把陣列傳進函式
//  傳陣列 = 傳第 0 格的地址（指標）
//  函式裡看起來是陣列，但本質是指標
// ──────────────────────────────────────
int array_sum(int* arr, int n) {
    int s{};
    for (int i = 0; i < n; ++i) s += arr[i];
    return s;
}

// ──────────────────────────────────────
//  競程用法 3：memset 快速清零 / 設初值
//  cstring 標頭裡，memset 以「位元組」為單位填值
//  0  → 每個位元組都填 0x00 → int 變成 0    ✔ 常用
//  -1 → 每個位元組都填 0xFF → int 變成 -1   ✔ 常用
//  其他數字通常不能直接用 memset 填 int
// ──────────────────────────────────────
void demo_memset() {
    int dp[5];
    memset(dp, 0, sizeof(dp));   // 全部清成 0
    cout << "dp[2] after memset 0 = " << dp[2] << '\n';

    memset(dp, -1, sizeof(dp));  // 全部設成 -1
    cout << "dp[2] after memset -1 = " << dp[2] << '\n';
}

// ──────────────────────────────────────
//  競程用法 4：雙指標（Two Pointers）技巧
//  這是「演算法技巧」，名字叫指標，但實際上
//  競程大多用「兩個索引 l, r」實作，而非真的 C++ 指標
//
//  範例：在排序好的陣列裡，找兩個數加起來 == target
// ──────────────────────────────────────
bool two_sum_sorted(int* arr, int n, int target) {
    int l = 0;        // 左索引，從最小值出發
    int r = n - 1;    // 右索引，從最大值出發
    while (l < r) {
        int sum = arr[l] + arr[r];
        if (sum == target) return true;
        else if (sum < target) ++l;  // 總和太小，左邊往右移
        else                  --r;   // 總和太大，右邊往左移
    }
    return false;
}

// ──────────────────────────────────────
//  競程用法 5：scanf 為什麼要加 &
//  scanf("%d", &x)  → 把 x 的地址傳給 scanf
//                   → scanf 透過那個地址把讀到的值填進 x
//  如果不加 & 就傳值，scanf 不知道要填到哪裡
// ──────────────────────────────────────
void demo_scanf_why_ampersand() {
    // 示範用 cin，概念相同：
    // cin >> x  內部也是透過 x 的地址來修改 x
    int x{};
    // scanf("%d", &x);  ← 標準寫法，& 就是傳地址
    // 這裡不讀輸入，只示範概念
    int* px = &x;  // px 存著 x 的地址
    *px = 77;      // 透過地址修改 x，和 scanf 做的事情一樣
    cout << "x = " << x << '\n';
}

// ──────────────────────────────────────
//  競程用法 6：vector 的 .data() 取得底層指標
//  有時要呼叫 C 函式（如 memcpy / sort）需要原始指標
// ──────────────────────────────────────
void demo_vector_data() {
    vector<int> v{5, 3, 1, 4, 2};
    int* raw = v.data();   // 取得底層陣列的指標
    cout << "v[0] 用 raw 讀取 = " << raw[0] << '\n';
    // sort, lower_bound 等也可以直接用 raw, raw+v.size()
}

// ──────────────────────────────────────
//  主程式：把全部用法串起來跑
// ──────────────────────────────────────
int main() {
    cout << "=== 用法 1：指標走訪陣列 ===\n";
    demo_array_traverse();

    cout << "\n=== 用法 2：陣列傳入函式 ===\n";
    int b[]{10, 20, 30};
    cout << "array_sum = " << array_sum(b, 3) << '\n';

    cout << "\n=== 用法 3：memset ===\n";
    demo_memset();

    cout << "\n=== 用法 4：雙指標找兩數之和 ===\n";
    int sorted[]{1, 2, 4, 7, 9};
    cout << "找 6 : " << (two_sum_sorted(sorted, 5, 6)  ? "找到" : "沒有") << '\n';
    cout << "找 10: " << (two_sum_sorted(sorted, 5, 10) ? "找到" : "沒有") << '\n';

    cout << "\n=== 用法 5：& 的本質（scanf 為什麼要加 &）===\n";
    demo_scanf_why_ampersand();

    cout << "\n=== 用法 6：vector.data() ===\n";
    demo_vector_data();

    return 0;
}

